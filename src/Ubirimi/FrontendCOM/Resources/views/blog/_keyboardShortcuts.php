<h2>Keyboard shortcuts</h2>
<p class="date">Published on: 15 DECEMBER, 2012</p>

<p>We just launched the release 1.619. It brings the usual bug fixing and you get keyboard shortcuts</p>

<p>
    c - create a new issue<br />
    e - edit current issue<br />
    g i - go to issue navigator<br />
    g a - go to all issues in the current selected project<br />
    g p - go to the summary page of the current selected project<br />
</p>

