                                <p itemprop="description" class="description">
                                    The straightforward method allows complex SLA metrics to be set up in just a few seconds with even fewer clicks.
                                    The metrics can be created with very complex criteria such as start, pause or stop.
                                    By selecting the applicable events the team is already up and running with the best level agreement on their side.
                                    The SLA targets are very easy to define. And size is not an issue – simply by applying the rules to specific subsets of issues,
                                    using the powerful YQL (Yongo Query Language), the project becomes as easy to read as a simple issue.
